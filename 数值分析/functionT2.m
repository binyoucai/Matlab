%定义辛普森符合梯形函数functionT2,传入参数f为被积函数,a、b为积分上下限，n为区间分割量
%函数开始
function [ T ] = functionT2( f,a,b,n )
%h的表达式
h=(b-a)/n;
%辛普森复合梯形公式累加部分1开始
E1=0;
for k=0:(n-1)
    xk=a+k*h;
    E1=E1+f(xk+h/2);
end
%辛普森复合梯形公式累加部分1结束

%辛普森复合梯形公式累加部分2开始
E2=0;
for k=1:(n-1)
    xk=a+k*h;
    E2=E2+f(xk);
end
%辛普森复合梯形公式累加部分2结束
%辛普森复合梯形总算法公式
T=(f(a)+4*E1+2*E2+f(b))*h/6;
%函数结束
end

